    public class WebViewClient extends android.webkit.WebViewClient {
        @Override
        public void onPageStarted(WebView view, String url, Bitmap favicon) {
            super.onPageStarted(view, url, favicon);
        }
        @RequiresApi(api = Build.VERSION_CODES.M)
        @Override
        public boolean shouldOverrideUrlLoading(WebView view, String url) {
            progressBar.setVisibility(View.VISIBLE);
            if (isInternetAvailable()||isNetworkConnected()) {

                if (url.startsWith("https://wekhed.ir")||url.startsWith("http://wekhed.ir")||url.startsWith("https://www.wekhed.ir")||url.startsWith("http://wekhed.ir")){
                    if (url.endsWith("/guest")){
                        view.loadUrl(url);
                    }
                    else if (url.contains("search/")){
                        view.loadUrl(url+"&android");
                    }
                    else {
                        view.loadUrl(url + "/?android");
                    }
                }
                else {
                    Intent intent = new Intent(Intent.ACTION_VIEW,Uri.parse(url));
                    intent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK);
                    startActivity(intent);
                }
            }
            else {
                startActivity(new Intent(MainActivity.this,ConnectionFailed.class));
                finish();
            }
            return true;
        }
        @Override
        public void onPageFinished(WebView view, String url) {
            super.onPageFinished(view, url);
            progressBar.setVisibility(View.GONE);
        }
    }
    @Override
    public boolean onKeyDown(int keyCode, KeyEvent event) {
        if (event.getAction() == KeyEvent.ACTION_DOWN) {
            switch (keyCode) {
                case KeyEvent.KEYCODE_BACK:
                    if (wekhedview.canGoBack()) {
                        wekhedview.goBack();
                    } else {
                        finish();
                    }
                    return true;
            }

        }
        return super.onKeyDown(keyCode, event);
    }
